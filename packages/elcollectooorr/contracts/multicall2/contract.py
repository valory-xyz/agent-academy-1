"""This module contains the scaffold contract definition."""
import asyncio
import concurrent.futures
import logging
import math
from typing import Any, List, Optional, cast

from aea.common import JSONLike
from aea.configurations.base import PublicId
from aea.contracts.base import Contract
from aea.crypto.base import LedgerApi
from aea.exceptions import enforce
from aea_ledger_ethereum import EthereumApi
from web3.types import BlockIdentifier


_logger = logging.getLogger("aea.packages.elcollectooorr.contracts.multicall2.contract")


class Multicall2Contract(Contract):
    """A wrapper for the MakerDAO Multicall2."""

    contract_id = PublicId.from_str("elcollectooorr/multicall2:0.1.0")

    @classmethod
    def get_raw_transaction(
        cls, ledger_api: LedgerApi, contract_address: str, **kwargs: Any
    ) -> JSONLike:
        """
        Handler method for the 'GET_RAW_TRANSACTION' requests.

        Implement this method in the sub class if you want
        to handle the contract requests manually.

        :param ledger_api: the ledger apis.
        :param contract_address: the contract address.
        :param kwargs: the keyword arguments.
        :return: the tx  # noqa: DAR202
        """
        raise NotImplementedError

    @classmethod
    def get_raw_message(
        cls, ledger_api: LedgerApi, contract_address: str, **kwargs: Any
    ) -> bytes:
        """
        Handler method for the 'GET_RAW_MESSAGE' requests.

        Implement this method in the sub class if you want
        to handle the contract requests manually.

        :param ledger_api: the ledger apis.
        :param contract_address: the contract address.
        :param kwargs: the keyword arguments.
        :return: the tx  # noqa: DAR202
        """
        raise NotImplementedError

    @classmethod
    def get_state(
        cls, ledger_api: LedgerApi, contract_address: str, **kwargs: Any
    ) -> JSONLike:
        """
        Handler method for the 'GET_STATE' requests.

        Implement this method in the sub class if you want
        to handle the contract requests manually.

        :param ledger_api: the ledger apis.
        :param contract_address: the contract address.
        :param kwargs: the keyword arguments.
        :return: the tx  # noqa: DAR202
        """
        raise NotImplementedError

    @classmethod
    def aggregate(
        cls,
        ledger_api: LedgerApi,
        contract_address: str,
        target_addresses: List[str],
        calldata: List[bytes],
    ) -> JSONLike:
        """
        Make aggregate call.

        :param ledger_api: the ledger apis.
        :param contract_address: the multicall address.
        :param kwargs: the keyword arguments.
        :return: the tx
        """
        instance = cls.get_instance(ledger_api, contract_address)
        res = instance.functions.aggregate(target_addresses, calldata).call()
        return dict(res=res)